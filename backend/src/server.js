const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
const morgan = require('morgan');
require('dotenv').config();

const app = express();

// Middleware
app.use(helmet());
app.use(cors({
  origin: process.env.ALLOWED_ORIGINS?.split(',') || '*'
}));
app.use(morgan('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Routes
app.get('/health', (req, res) => {
  res.json({ status: 'OK', message: 'IFTC Backend API is running' });
});

// Import routes
const shipmentRoutes = require('./routes/shipment.routes');
const cargoRoutes = require('./routes/cargo.routes');
const agentRoutes = require('./routes/agent.routes');
const customsRoutes = require('./routes/customs.routes');

// Use routes
app.use('/api/shipments', shipmentRoutes);
app.use('/api/cargo', cargoRoutes);
app.use('/api/agents', agentRoutes);
app.use('/api/customs', customsRoutes);

// Error handling middleware
app.use((err, req, res, next) => {
  console.error(err.stack);
  res.status(err.status || 500).json({
    error: {
      message: err.message || 'Internal Server Error',
      status: err.status || 500
    }
  });
});

// 404 handler
app.use((req, res) => {
  res.status(404).json({ error: 'Route not found' });
});

const PORT = process.env.PORT || 3000;

app.listen(PORT, () => {
  console.log(`ðŸš€ IFTC Backend API running on port ${PORT}`);
  console.log(`ðŸ“Š Environment: ${process.env.NODE_ENV || 'development'}`);
  console.log(`ðŸ”— Health check: http://localhost:${PORT}/health`);
});

module.exports = app;
